// Algoritmo de estrutura condicinonal switch:

public class Switch {
    public static void main (String[] args){
        int x = 15;
        switch (x){
            case 9:
            System.out.println("O valor é: 9");
            break;
            case 10:
            System.out.println("O valor é: 10");
            break;
            case 12:
            System.out.println("O valor é: 12");
            break;
            case 15:
            System.out.println("O valor é: 15");
            break;
            default: 
            System.out.println("Nenhum valor");
        }
    }
}
