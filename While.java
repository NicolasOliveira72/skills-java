// Algoritmo com estrutura de repetição while:
public class While {
    public static void main (String[] args){
        // Declaração da variável "x" para a execução do while:
        int x = 5;
        // While com a variável "x" declarada:
        while (x <= 10) {
            System.out.println("O número é: " + x);
            System.out.println("-----------------");
            x++;
        }
    }
}
