// Algoritmo de operador ternário:

public class Ternario {
    public static void main(String[] args){
        // Variável "x" declarada para comparação:
        int x = 14;
        // Operador ternário para comparar com a variável declarada:
        String resultado = (x == 12) ? "Correto" : "Errado";
        // Imprimindo o resultado da análise:
        System.out.println(" A resposra é: " + resultado);
    }
}
